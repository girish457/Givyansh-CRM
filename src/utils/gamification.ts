export const getBadgeForCoins = (coins: number, isTL: boolean = false) => {
  const tiers = isTL ? TLTeamCoinBadges : RecruiterCoinBadges;
  return getTierData(coins, tiers);
};

export const getBadgeForSelections = (selections: number, isTL: boolean = false) => {
  const tiers = isTL ? TLTeamSelectionBadges : RecruiterSelectionBadges;
  return getTierData(selections, tiers);
};

export const getBadgeForJoinings = (joinings: number, isTL: boolean = false) => {
  const tiers = isTL ? TLTeamJoiningBadges : RecruiterJoiningBadges;
  return getTierData(joinings, tiers);
};

const getTierData = (value: number, tiers: any[]) => {
  let currentBadge = tiers[0];
  let nextBadge = tiers.length > 1 ? tiers[1] : null;

  for (let i = 0; i < tiers.length; i++) {
    if (value >= tiers[i].min) {
      currentBadge = tiers[i];
      nextBadge = i + 1 < tiers.length ? tiers[i + 1] : null;
    } else {
      break;
    }
  }

  return { currentBadge, nextBadge };
};
